export const getInternshipById = (id) => {
  return dummyData.internships.find(internship => internship.id === id);
};

export const getInternshipsByCompany = (companyId) => {
  return dummyData.internships.filter(internship => internship.companyId === companyId);
};

export const getStudentInternships = (studentId) => {
  const student = dummyData.students.find(s => s.id === studentId);
  if (!student) return [];
  
  return student.appliedInternships.map(app => ({
    ...getInternshipById(app.internshipId),
    status: app.status
  }));
};

// Function to add a new company (for pending approvals)
export const addCompany = (company) => {
  const newId = Math.max(...dummyData.companies.map(c => c.id)) + 1;
  dummyData.companies.push({
    ...company,
    id: newId,
    isApproved: false
  });
  return newId;
};

// Function to approve a company
export const approveCompany = (companyId) => {
  const company = dummyData.companies.find(c => c.id === companyId);
  if (company) {
    company.isApproved = true;
  }
};

// Function to reject a company
export const rejectCompany = (companyId) => {
  dummyData.companies = dummyData.companies.filter(c => c.id !== companyId);
};

// Function to apply for an internship
export const applyForInternship = (studentId, internshipId) => {
  const student = dummyData.students.find(s => s.id === studentId);
  const internship = dummyData.internships.find(i => i.id === internshipId);
  
  if (student && internship) {
    // Add to student's applied internships
    student.appliedInternships.push({
      internshipId,
      status: "applied"
    });
    
    // Add to internship's applicants
    internship.applicants.push({
      studentId,
      status: "applied"
    });
    
    return true;
  }
  return false;
};

// Function to update internship application status
export const updateApplicationStatus = (studentId, internshipId, newStatus) => {
  const student = dummyData.students.find(s => s.id === studentId);
  const internship = dummyData.internships.find(i => i.id === internshipId);
  
  if (student && internship) {
    // Update student's application status
    const studentApp = student.appliedInternships.find(app => app.internshipId === internshipId);
    if (studentApp) {
      studentApp.status = newStatus;
      if (newStatus === "completed") {
        student.completedInternships.push(internshipId);
      }
    }
    
    // Update internship's applicant status
    const internshipApp = internship.applicants.find(app => app.studentId === studentId);
    if (internshipApp) {
      internshipApp.status = newStatus;
    }
    
    return true;
  }
  return false;
}; 
